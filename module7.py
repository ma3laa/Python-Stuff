#Sorting and reversing lists

colors = ["blue", "red", "green", "yellow"]
colors.sort()
print(colors)

#sorts in alphabetical order - A to Z

colors.sort(reverse=True)
print(colors)

#sorts in reverse alphabetical order - Z to A

alpha = ["a", "c", "e", "b", "d"]
alpha.reverse()
print(alpha)

