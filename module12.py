#For loops

colors = ["blue", "red", "pink", "green", "yellow", "white", "black", "orange"]

for color in colors:
    print(color)



for a in range(10):
    print(a)

for x in range(10,35):
    print(x)

for y in range(0,100,2):
    print(y)


#working with range(), you can pass between 1 and 3 integer arguments to it
#1st = starting point or up to that amount
#2nd = end point
#3rd = how many you skip


